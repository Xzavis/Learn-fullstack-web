// Variables = Data Containers

// JavaScript variables are containers for data.

// JavaScript variables can be declared in 4 ways:
// Modern JavaScript

//     Using let
//     Using const

// Older JavaScript

//     Using var (Not Recommended)
//     Automatically (Not Recommended)

// contoh let
let name = "john doe";
let age = 30;
let data = `halo nama saya ${name} umur saya ${age} tahun`;
console.log(data);

const x = 10;
const y = 20;
const z = `Hasil Dari ${x} + ${y} = ${x + y}`;
console.log(z);

let truckName;
truckName = "ford";
console.log(`truckName: ${truckName}`); 

const carName = "toyota";
console.log(`carName: ${carName}`);

let aA = 10,
    n = 30,
    xn = aA + n;
console.log(`Hasil Dari ${aA} + ${n} = ${xn}`);